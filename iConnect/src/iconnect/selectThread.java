    /**
     * method to create chat threads
     */
    private void createChatThread() {

        try {
            // sql statement to get all deatails of thread table
            String sql = "select * from threads";
            PreparedStatement prepareSta = conn.prepareStatement(sql);
            ResultSet resultSet = prepareSta.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel) chatThread.getModel();
            dtm.setColumnCount(4);
            System.out.println(sql);

            while (resultSet.next()) {
                Object object[] = {resultSet.getInt("threadId"), resultSet.getString("threadTitle"), resultSet.getString("LastEdited"), resultSet.getString("User")};

                dtm.addRow(object);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

