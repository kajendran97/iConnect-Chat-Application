    private void chatThreadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_chatThreadMouseClicked

        // set jTable to Model
        DefaultTableModel dtm = (DefaultTableModel) chatThread.getModel();
        int threadSelect = chatThread.getSelectedRow();
        selectedThread = Integer.parseInt(dtm.getValueAt(threadSelect, 0).toString());
        
        // check selected thread by threadId
        System.out.println("Selected thread :" + selectedThread);
    }//GEN-LAST:event_chatThreadMouseClicked

