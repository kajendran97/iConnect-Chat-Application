    private static boolean loginUI(java.lang.String loginId, java.lang.String password) {
        iconnect.IConnectChatApp_Service service = new iconnect.IConnectChatApp_Service();
        iconnect.IConnectChatApp port = service.getIConnectChatAppPort();
        return port.loginUI(loginId, password);
    }

    /**
     * method to decrypt password
     * @param inputPassword
     * @param key
     * @return 
     */
    public static String decryptPasswordVal(String inputPassword, int key) {

        inputPassword = inputPassword.toLowerCase();
        String c = "";

        for (int i = 0; i < inputPassword.length(); i++) {

            int pos = strAll.indexOf(inputPassword.charAt(i));
            int keyValue = (pos + key) % 26;
            char repValue = strAll.charAt(keyValue);
            c = c + repValue;
        }
        return c;
    }

