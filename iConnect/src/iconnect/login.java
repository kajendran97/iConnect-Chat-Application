    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed

        // assign loginId to the static variable
        userLogged = loginId.getText();

        // assign password to the static variable
        inputPassword = password.getText();

        // check for empty fields
        if (userLogged.equals("") || inputPassword.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill all the required fileds...!", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // decrypting the password
        String decryptedPass = decryptPasswordVal(inputPassword, 3);;

        // call loginUI() from server
        if (loginUI(userLogged, decryptedPass)) {

            this.dispose();
            JOptionPane.showMessageDialog(null, "Login Successful", "Success", JOptionPane.INFORMATION_MESSAGE);
            
            // direct to select thread if login is successful
            selectThread st = new selectThread();
            st.setVisible(true);

        } else {

            JOptionPane.showMessageDialog(null, "Login Id or Password is Incorrect...!");
        }


    }//GEN-LAST:event_loginBtnActionPerformed

