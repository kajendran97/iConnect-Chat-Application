    private void addThreadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addThreadBtnActionPerformed
        
        int threadSelected = selectedThread;
        String inputMessage = messageTextField.getText();
        String editedTime = new SimpleDateFormat("yyyy/MM/dd || HH:mm:ss").format(Calendar.getInstance().getTime());
        String inputUser = userLogged;

        // call threadEdit() from server
        threadEdit(threadSelected,inputMessage,editedTime,inputUser);
        
        editThread cleanUp = new editThread();
        this.dispose();
        cleanUp.setVisible(true);

    }//GEN-LAST:event_addThreadBtnActionPerformed

