    private void messageThreadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_messageThreadMouseClicked


        // set jTable to DefaultTableModel
        DefaultTableModel dtm = (DefaultTableModel) messageThread.getModel();
        int threadSelect = messageThread.getSelectedRow();
        selectedThread = Integer.parseInt(dtm.getValueAt(threadSelect, 0).toString());

        // check selected thread by threadId
        System.out.println("Selected thread :" + selectedThread);
    }//GEN-LAST:event_messageThreadMouseClicked

