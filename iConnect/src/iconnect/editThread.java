    /**
     * method to add message to database
     */
    public void createMessage() {

        try {

            // sql statement to select message details
            String sql = "SELECT message, lastEditedTime, sentUser FROM `messages` WHERE `threadId` = '" + selectedThread + "'";
            PreparedStatement ps = conn.prepareStatement(sql);

            ResultSet resultSet = ps.executeQuery();
            DefaultTableModel dt = (DefaultTableModel) messageThread.getModel();
            dt.setColumnCount(3);
            System.out.println(sql);

            while (resultSet.next()) {
                Object obj[] = {resultSet.getString("message"), resultSet.getString("lastEditedTime"), resultSet.getString("sentUser")};
                System.out.println(resultSet.getString("message") + resultSet.getString("lastEditedTime") + resultSet.getString("sentUser"));
                dt.addRow(obj);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

